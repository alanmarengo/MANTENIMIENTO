<script type='text/javascript'>
$(document).ready(function() {
    $('map').imageMapResize();

    $("map area").click( function () {
        showFicha($(this).attr('id'));
        return false;
    });

    function showFicha(id) {
        var item = caminito.find(x => x.id === id);
        
        $('#uxPopTitulo').html(item.titulo);
        $('#uxPopTexto').html(item.texto);
        $('#uxVerFicha').data('target', '#_' + item.id);
        $('#uxLinkGeovisores').attr('href', item.link_geovisor)
        
        $('#uxFicha').modal('show');
    }

    // ARMADO DE LAS FICHAS
    caminito.forEach(item => {
        let html = `
            <div class="col-md-12 ficha">
                <a id="_${item.id}"></a>    
                <h3>${item.titulo}</h3>
                <p>${item.texto}</p>
            </div>
        `;

        $('#uxFichas').append(html);
    });

    $('#uxVerFicha').on('click', function () {
        let target = $(this).data('target');
        $('html, body').animate({
            scrollTop: $(target).offset().top - 200
        }, 500)
    });

    $('.section-sticky a').on('click', function() {
        $('.section-sticky a').removeClass('selected');
        $(this).addClass('selected');

        let selector = $(this).data('target');
        $('html, body').animate({
            scrollTop: $(selector).offset().top - 200
        }, 500)
    });

    let target = $.urlParam('target');
    if (target) {
        // LINKS MENU TOP
        $('#link-' + target).trigger('click');
    }

    let pop = $.urlParam('pop');
    if (pop) {
        // APERTURA POP DE CAMINITO DESDE QS
        $('#link-temas').trigger('click');
        showFicha(pop);
    }

    $('.pop-button2').hover( 
        function () {
            let key=$(this).data('key');
            $(this).css('background-image', 'url("./images/icono-' + key + '-relleno-hover.png")')
        },
        function () {
            let key=$(this).data('key');
            $(this).css('background-image', 'url("./images/icono-' + key + '-relleno.png")')
        }
    )

});
</script>