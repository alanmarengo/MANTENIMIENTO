                    La Constitución Nacional reconoce la preexistencia étnica y cultural de los pueblos indígenas argentinos y reconoce la personería jurídica de las comunidades. A su vez, el Convenio 169 de la Organización Internacional del Trabajo sobre Pueblos Indígenas (Ley Nacional 24.017) contempla la consulta a los pueblos interesados cada vez que se prevén medidas legislativas o administrativas susceptibles de afectarles directamente.
                    <br />
                    <br />
                    A partir de una sentencia judicial del 20 de octubre de 2017 se constituye la mesa de diálogo para hacer efectivo el proceso de consulta para los aprovechamientos hidroeléctricos del río Santa Cruz, en la que participan las 15 comunidades de pueblos originarios de la provincia, los Estados Nacional y Provincial, el Instituto Nacional de Asuntos Indígenas y la empresa contratista. 
                    <br />
                    <br />
                </div>
                <div class="col-md-6 text-justify">
                    En ese marco, se realizan reuniones plenarias con diferente periodicidad, reuniones de las comunidades con sus asesores, se firmaron convenios de cooperación y se llevan adelante actividades conjuntas, en un proceso abierto que tiene como objetivo construir una mirada intercultural sobre el proyecto, la naturaleza y las ideas de desarrollo, y el reconocimiento de los derechos indígenas. 
