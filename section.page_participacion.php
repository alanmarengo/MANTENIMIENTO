                    <br />
                    <br />
                    Las articulaciones que sustentan la ejecución del proyecto operan también en diversos niveles. Además de los indicados, podemos mencionar: 
                    <br />
                    <br />
                </div>
                <div class="col-md-6 text-justify">
                    - La provisión de tecnología, conocimiento y experiencias de gestión de diversas industrias y expertos de todo el mundo; 
                    <br />
                    - La gestión de programas ambientales con la participación del sistema científico argentino y de expertos internacionales; 
                    <br />
                    - La provisión de insumos, materiales y tecnologías para la obra de distintos puntos del país, incluyendo la propia provincia de Santa Cruz;
                    <br />
                    - La interacción con organismos y agencias nacionales con competencia en el área de proyecto o en temas relacionados; 
                    <br />
                    - El otorgamiento de permisos para gestionar distintos aspectos de la obra o del Plan de Gestión Ambiental por parte de organismos nacionales y provinciales. 
                    <br />
                    - La articulación con agencias y organismos provinciales para el monitoreo de diversas variables sociales y ambientales. 
                    <br />
                    - La interacción –ya sea a nivel de espacios de diálogo o en ámbito judicial con actores u organizaciones que cuestionan el proyecto o alguno de sus aspectos. 
                    <br />
                    - La relación con instituciones encargadas de la gestión de la energía, el agua, la seguridad de presas y la gestión de riesgos. 
