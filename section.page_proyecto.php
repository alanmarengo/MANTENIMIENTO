            <h3 style="margin-bottom: 26px;">La Barrancosa</h3>

            <div class="row" style="padding-left: 2em; padding-right: 2em;">
                <div class="col-md-6 parrafos">
                    La presa La Barrancosa tiene características similares a CC, pero con una longitud mayor,
                    de 2.445 m, y las estructuras principales estarán ubicadas en margen derecha. Estará
                    ubicada a 65 km aguas abajo de CC.
                    <br />
                    <br />
                    El vertedero posee cinco vanos de 12 m cada uno, con capacidad de descarga de 4.163
                    m3/s (con una recurrencia de 10.000 años). Dentro de la estructura del vertedero se
                    ubican los conductos y dispositivos de cierre que constituyen el descargador de fondo con
                    capacidad para 700 m3/s.
                    <br />
                    <br />
                    La obra de toma se encuentra integrada a la casa de máquinas y está equipada con un
                    esquema de rejas, ataguías de mantenimiento y cierre de emergencia, similares a las de
                    CC.
                    <br />
                    <br />
                    La casa de máquinas se compone de tres módulos de 28 m de ancho cada uno y sus
                    respectivas áreas de montaje, servicios e instalaciones auxiliares de generación. Aloja tres
                    turbinas del tipo Kaplan de 120 MW cada una (360 MW en total). El régimen de operación
                    es de base y la generación anual estimada es 1.903 GWh/año. Atendiendo a
                    consideraciones ambientales, la operación de base consiste en erogar la misma cantidad
                    de agua que ingresa al río desde el lago Argentino en cada momento, de modo tal que
                    aguas abajo de la presa (que incluye la zona del estuario) el río se comporte de la misma
                    manera que si ambas obras no existiesen. De esta manera, la central operará las 24 horas
                    del día, generando la energía que el caudal medio del río le permita y manteniendo los
                    caudales naturales horarios del río Santa Cruz.
                    <br />
                    <br />
                </div>
                <div class="col-md-6 parrafos">
                    La escala de peces es equivalente a nivel conceptual a la de la presa CC. Se ubica sobre la
                    margen derecha, próxima a la casa de máquinas.
                    <br />
                    <br />
                    <b>Desvío del río</b>
                    <br />
                    <br />
                    El proceso de desvío tiene las mismas etapas que las señaladas para CC. La obra se
                    compone de un canal a cielo abierto de sección trapezoidal de 120 m de ancho. En LB, el
                    canal de desvío se transformará también en descargador de fondo, pero en este caso
                    estará incorporado a la estructura del vertedero.
                    <br />
                    <br />
                    Las presas se emplazarán en el tramo definido entre el lago Argentino y un punto ubicado
                    a 135 km aguas arriba de la localidad de Comandante Luis Piedrabuena. El río Santa Cruz,
                    de 385 km de extensión, cruza la provincia homónima de oeste a este, partiendo del lago
                    Argentino, a una altitud de 180 metros sobre el nivel del mar hasta su desembocadura en
                    el océano Atlántico. La cuenca de este río tiene una superficie de 25.000 km2.
