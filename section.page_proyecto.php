                    <p class="bullet-parrafo">
                        Disminución en los niveles del embalse de la presa Cóndor Cliff a los efectos de la desvinculación hidráulica en la operación de los Aprovechamientos del Río Santa Cruz con los niveles del Lago Argentino, asegurando la no afectación de los glaciares existentes en el mismo.
                    </p>

                    <img src="./images/bullet_blue.png" class="bullet" />
                    <p class="bullet-parrafo">
                        Establecer, como criterio de operación, que el nivel del embalse de la presa La Barrancosa debe alcanzar en todo momento al pie de la presa Cóndor Cliff y la permanencia del medio acuático entre ambos cierres, evitando eventuales afectaciones a la fauna ictícola en este sector.
                    </p>

                    <img src="./images/bullet_blue.png" class="bullet" />
                    <p class="bullet-parrafo">
                        Modificación de la forma de operación de la presa La Barrancosa, prevista empuntada a un régimen de base, de forma tal que el caudal erogado sea el mismo que el medido en la naciente del río Santa Cruz, preservando condiciones de hábitat equivalentes a la situación sin embalses, en el curso inferior y estuario del río Santa Cruz. Este cambio en la operación del complejo resulta altamente significativo para el proyecto, y fue acompañado por una reducción en la cantidad de turbogeneradores a ser instalados, se pasó de 6 a 5 unidades en CC y de 5 a 3 en LB.
                    </p>

                    <img src="./images/bullet_blue.png" class="bullet" />
                    <p class="bullet-parrafo">
                        Redefinición de los criterios de cálculo de la altura de la ola de diseño en la presa CC, posibilitando una disminución de 6,5 m en la cota del coronamiento, disminuyendo en forma sensible la superficie a inundar por el embalse de esta presa.
                    </p>
                </div>

                <div class="col-md-6 parrafos">
                    <img src="./images/bullet_blue.png" class="bullet" />
                    <p class="bullet-parrafo">
                        Incremento de los caudales máximos que resulta posible evacuar a través de los descargadores de fondo, en ambas presas, de 180 m3/s hasta alcanzar 700 m3/s. Este incremento en la capacidad de evacuación permite mayor flexibilidad en el mantenimiento de los caudales aguas abajo de las presas durante el llenado de embalses, ante eventuales salidas de servicio de la central y en caso de ser requerido un vaciado parcial del embalse, como una herramienta de manejo del riesgo en la seguridad de las presas. Complementariamente, con la mayor disponibilidad de caudales a erogar, resulta posible disminuir la velocidad del flujo hacia aguas abajo, durante la etapa de llenado de los embalses, preservando y o disminuyendo la afectación a la ictiofauna.
                    </p>

                    <img src="./images/bullet_blue.png" class="bullet" />
                    <p class="bullet-parrafo">
                        Incorporación al proyecto de una línea de extra alta tensión (LEAT) 500 kV y las adecuaciones necesarias con el fin de vincular los aprovechamientos al Sistema Argentino de Interconexión, en la Estación Transformadora 500/132 kV río Santa Cruz.
                    </p>

                    <img src="./images/bullet_blue.png" class="bullet" />
                    <p class="bullet-parrafo">
                        Modificaciones en el acceso vial permanente norte de la presa LB y en los accesos permanentes norte y sur al sector de la presa CC, a efectos de minimizar posibles impactos ambientales y lograr su adecuación a las características de los caminos existentes. Particularmente, las modificaciones en el acceso norte a CC permiten preservar un sector detectado como refugio de cóndores (condoreras).
                    </p>
