                <div class="col-md-6 text-justify">
                    El río Santa Cruz posee el mayor potencial hidroeléctrico aprovechable de los ríos
                    interiores de nuestro país. Tiene particularidades que optimizan su aprovechamiento,
                    como su doble regulación natural. En primer lugar, al ser un río de aporte glaciario, su
                    caudal no depende solo de las precipitaciones estacionales, sino de la radiación solar que
                    fusiona los hielos. En segundo lugar, el lago Argentino hace las veces de un embalse,
                    regulando el caudal del río. Esta regularidad en el comportamiento hidráulico del río hace
                    posible que el diseño operativo de las obras permita evitar la afectación sobre el estuario,
                    ya que la central La Barrancosa descargará en forma permanente el mismo caudal que
                    ingresa desde el lago Argentino al río Santa Cruz.
                    <br />
                    <br />
                    La presa CC estará ubicada en la progresiva 253 del río, a 170 km de la ciudad de Calafate,
                    mientras que la presa LB estará ubicada en la progresiva 187, a 135 km de la ciudad de
                    Comandante Luis Piedrabuena.
                    <br />
                    <br />
                    En su nivel máximo de operación normal (NAON), la presa CC generará un embalse de
                    238,5 km2 de superficie (equivalente al 16 % de la superficie del lago Argentino). El
                    embalse LB, según la cota NAON, tendrá una superficie 190 km2 (equivalente al 13 % de la
                    superficie del mismo lago).
                    <br />
                    <br />
                    A diferencia de otros proyectos hidroeléctricos, los embalses no requieren
                    reasentamiento de población. Ello implica que se reduzcan sensiblemente las
                    complejidades sociales del emprendimiento.
