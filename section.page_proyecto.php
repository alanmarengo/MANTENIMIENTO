                    <h5>Caminos de acceso sobre ambas márgenes</h5>
                    <div class="resumen">
                        Realizar la conexión terrestre del aprovechamiento con la red vial existente.
                    </div>
                    <p>
                        El camino de Acceso a la presa La Barrancosa se desenvuelve, desde la ruta nacional Nº 9, por la meseta de margen derecha de nivel +145,00 msnm y desciende por la ladera que lleva al valle fluvial del rio Santa Cruz hasta empalmar con el coronamiento de la presa (nivel 118,50 msnm). A través de este último, se vincula con la margen izquierda ascendiendo a la meseta ubicada en nivel +170,00 msnm donde pone fin a su recorrido, con una longitud, descontando la longitud del coronamiento, de 4.430 m aproximadamente. 
                        <br />
                        Dentro de la obra también se construirán caminos de acceso permanentes, a los distintos sectores de la obra, para el ejercicio normal de la operación y mantenimiento de todas las instalaciones.
                        <br />
                    </p>

