                <div class="col-md-6 parrafos">
                    <h5>Vertedero.</h5>
                    <div class="resumen">
                        Órgano de seguridad, destinada a realizar manejo de caudales excedentes. Se equipa con compuertas.
                    </div>
                    <p>
                        El vertedero se ubica sobre la margen izquierda, en el extremo del cierre. La longitud es de 115 m, la cota de la cresta se encuentra a cota 167,50 msnm, y se compone de 4 vanos. La capacidad de descarga total es de 4.163 m3/s. Posee compuertas de sector que se accionan mediante mecanismos hidráulicos. A su vez, se dispone de recatas que permiten la colocación de ataguías para realizar tareas de mantenimiento. Sobre el vertedero se encuentra un puente de servicio.                    
                    </p>

                    <h5>Casa de máquinas.</h5>
                    <div class="resumen">
                        Estructura de hormigón armado, con una superestructura de cerramiento, a partir del nivel de techo de los generadores que se materializa mediante una estructura metálica. Es donde se instalan las turbinas, encargadas de transformar la energía potencial del agua, en energía eléctrica.  
                    </div>
                    <p>
                        Se ubica a pie de presa, y se compone de un sector principal constituido por 5 módulos de 27 m de ancho cada uno, donde se alojan los 5 grupos turbogeneradores, cada uno de 180 MW, la nave de montaje de 48 m y una nave auxiliar de servicios de 12 m de ancho. Hacia aguas abajo, por encima del nivel de generadores se encuentra el sector de emplazamiento de transformadores y por debajo las galerías eléctricas y mecánicas.
                        <br />
                        El acceso se realiza desde el extremo de margen derecha, donde se ubica el ingreso a la nave de montaje, y al edificio que donde se brinda soporte a las operaciones, como así también la sala de control. 
                        <br />
                        A nivel 123,77 msnm, se encuentra la sala de generadores. Por sobre la misma se encuentra un puente grúa, equipado con un gancho principal y un gancho auxiliar, que se desplaza sobre las unidades y la nave de montaje. Este equipamiento se utiliza tanto para el montaje como para tareas de mantenimiento. En el sector exterior, se ubican 3 transformadores con sus respectivos pórticos de salida. Hacia aguas abajo se encuentra un pórtico grúa que se utiliza para la colocación de las ataguías del tubo de aspiración, cuando así lo requieran las tareas de mantenimiento.
                        <br />
                        En niveles inferiores se ubican dos galerías eléctricas, donde desarrollan su recorrido las barras de salida hacia los transformadores. Adicionalmente se ubican los diversos equipos eléctricos, como tableros, que acompañan a las instalaciones de este tipo. En un nivel inferior se ha proyectado una galería mecánica. En la misma se distribuyen los distintos sistemas auxiliares como por ejemplo, circuitos de aire comprimido, aceite, agua, etc. A su vez, se dispone de áreas que podrán ser utilizadas como talleres, tanto eléctricos como mecánicos o por ejemplo, pañoles donde se almacenan repuestos.                        
                        <br />
                    </p>

                    <h5>Escala de peces.</h5>
                    <div class="resumen">
                        Tiene por objetivo garantizar la migración hacia aguas arriba de la fauna ictica ante el obstáculo que representa la presa. 
                    </div>
                    <p>
                        En este caso, se ha optado por el siguiente esquema:
                        <br />
                            &nbsp;&nbsp;&nbsp;&nbsp;• Un escala que consta de un canal, formado por una sucesión de estanques con vertederos sucesivos hasta la cota 174,36 msnm
                        <br />
                            &nbsp;&nbsp;&nbsp;&nbsp;• Este canal alcanza una esclusa que permite salvar el desnivel entre la cota mencionada, y los diferentes niveles del embalse
                        <br />
                        La traza de la escala se ubica sobre margen izquierda, apoyada sobre el espaldón aguas abajo de la presa y en parte sobre terraplén en el mismo sector, con una pendiente longitudinal del 10 %. Cada estanque es de 6,00 m de largo por 4,00 m de ancho libre. Cada tabique transversal que se materializa entre estanques, posee un vertedero de 0,70 m ubicado a 2,10 m medido desde el fondo de la escala. El valor del tirante medio es de 3,20 m y la altura total es de 3,90 m. El caudal de diseño de la escala es de 3m3/s. 
                        <br />
                        La estructura de la esclusa consta de un recinto, controlado por compuertas planas en sus extremos. La operación de la esclusa permite recibir los peces dentro del recinto y, mediante el cierre de la compuerta aguas abajo y la posterior apertura de la compuerta aguas arriba, entregar los peces al embalse para los diversos niveles en que el mismo se encuentre. Lateralmente a esta estructura, se encuentra un sistema de tuberías que se utiliza para el llenado y vaciado de la esclusa. Cabe destacar, que también tiene la función de garantizar el caudal continuo en la escala, permitiendo el ascenso continuo de los peces hasta la misma.
                        <br />
                    </p>

                    <h5>Caminos de acceso sobre ambas márgenes</h5>
                    <div class="resumen">
                        Realizar la conexión terrestre del aprovechamiento con la red vial existente.
                    </div>
                    <p>
                        El acceso a la presa CC se concibe desde un camino que se construirá vinculando ambas márgenes del río Santa Cruz, específicamente desde la RP9 al sur y conectando con la RP17 al norte del cierre.
                        <br />
                        La traza definitiva del camino fue definida, en el acceso norte de la misma, con un nuevo trazado, sin afectar las condoreras identificadas en la margen izquierda de la presa.
                        <br />
                    </p>

