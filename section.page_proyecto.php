                    <p style="font-size: 14px;">
                        Si bien el sitio de emplazamiento de los cierres responde a condiciones geológicas y geométricas que no se han modificado desde los estudios realizados por Agua y Energía en la década del ’70, el proyecto licitado presentaba varias cuestiones que no se encontraban adecuadamente resueltas y que debieron ser modificadas con posterioridad a la adjudicación e inicio de las obras. Estas modificaciones resultan de fundamental importancia para garantizar la sustentabilidad del proyecto. En este sentido, se destacan los siguientes cambios respecto del proyecto tomado como referencia en la licitación.
                    </p>
                    <img src="./images/proyecto_evolucion.jpg" style="width: 100%; height: auto; padding: 20px 100px 40px 100px;" />
