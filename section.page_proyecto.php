            <h3>DESCRIPCIóN</h3>

            <div class="row">
                <div class="col-md-6 text-justify">
El Proyecto de los “Aprovechamientos Hidroeléctricos del Río Santa Cruz” está integrado por dos cierres; la presa Cóndor Cliff y la presa La Barrancosa.
<br />
<br />
La presa Cóndor Cliff, de materiales sueltos con pantalla impermeable de hormigón o CFRD, por sus siglas en inglés, se desarrolla en una longitud de 1.613 m y alcanza una altura de 68 m. La casa de máquinas ubicada sobre la margen izquierda, cuenta con 5 turbinas del tipo Francis de 190 MW cada una. Con esta configuración, es posible turbinar un caudal máximo de 1.750 m3/s lo que implica una generación media anual de 3.268 GWh, operando en régimen de punta o sea generando solo en las horas de mayor demanda energética.
<br />
<br />
A 65 km aguas abajo del cierre anteriormente mencionado, se ubica la presa La Barrancosa. Esta presa también es del tipo CFRD y se desarrolla en una longitud de 2.445 m alcanzando una altura de 41 m. En este caso, la casa de máquinas se ubica sobre la margen derecha y aloja 3 turbinas del tipo Kaplan de 120 MW cada una. Considerando que el caudal turbinable es de 1.260 m3/s, la generación anual estimada es de 1.903 GWh/año. Contempla un regimen de operación de base, con un caudal permanente igual al saliente del Lago Argentino.
