        <div class="col-md-12 section-a barrancosa">
            <a id="barrancosa"></a>
            <h3 style="margin-bottom: 6px;">La Barrancosa</h3>
            <h4>Descripción de cada componente</h4>
            
            <div class="row" style="padding-left: 2em; padding-right: 2em;">
                <div class="col-md-6 parrafos">
                    <h5>Presa de materiales sueltos.</h5>
                    <div class="resumen">
                        Es la estructura que constituye el cierre transversal del río, mediante la cual es posible elevar el nivel natural del río, y contener el embalse.
                    </div>
                    <p>
                        La presa en el cierre de LB es del tipo CFRD (concrete face rockfill dam), al igual que lo enunciado para el caso de CC. En este caso, desarrolla una longitud de 2.445,25 m. El coronamiento posee un ancho de 12 m y se ubica a un nivel de 118,50 msnm. La altura máxima sobre el cauce es de 43,50 m. Se han adoptado taludes 1V:1,5H conforme la experiencia en proyectos características similares. El volumen de terraplén que constituye el cuerpo de la presa es de aproximadamente 10 millones de metros cúbicos y se encuentra zonificado mediante distintos tipos de materiales que tienen como característica permitir el drenaje de posibles filtraciones e impedir el arrastre de materiales finos y la perdida de resistencia estructural.
                        <br/>
                        La fundación de la presa se realiza sobre el cauce del río, y se prevé retirar una capa superficial de 2 m de espesor. Dada la profundidad de la roca y espesor del aluvión, el cierre se resuelve mediante la ejecución de un muro colado de hormigón armado de 0,80 m de espesor construido mediante paneles de 6,00 m de ancho que penetran la roca en una profundidad que se ajustará con el desarrollo de la ingeniería de detalle, estimada en aproximadamente 5 m. 
                        <br/>
                        Sobre la margen derecha, inmediatamente a continuación de la casa de máquinas, el cierre se materializa mediante un muro de gravedad de hormigón. El mismo se extiende en una longitud de 90 m. 
                        <br/>
                    </p>

                    <h5>Obras de toma para la casa de máquinas.</h5>
                    <div class="resumen">
                        Estructura destinada a conducir los caudales necesarios para la generación eléctrica a través de la tubería forzada hacia la casa de máquinas 
                    </div>
                    <p>
                        La obra de toma tiene la función de captar el agua y posteriormente, mediante las tuberías forzadas conducirla hacia la casa de máquinas. Se compone de 5 módulos de 25 m de ancho, uno por cada turbina. En cada módulo de esta estructura se instala una reja inclinada, que evita la intrusión de objetos sólidos hacia las máquinas. Inmediatamente aguas abajo, se presenta un vano que posibilita colocar ataguías para realizar tareas de mantenimiento. A continuación, se encuentra el cierre de emergencia, constituido por compuertas planas con ruedas que tienen capacidad para cortar el flujo. La misma se acciona mediante un pistón hidráulico. Luego, el caudal recorre una transición desde una sección rectangular hacia la sección circular de 9 m de diámetro, coincidente con las dimensiones de la tubería forzada, que posee una longitud de aproximadamente 100 m hasta su conexión con las turbinas ubicadas en la casa de máquinas.
                    </p>

                    <h5>Descargadores de fondo.</h5>
                    <div class="resumen">
                        Órgano de seguridad, que tiene como función evacuar caudales a través del sector inferior del cierre. Se utiliza para realizar vaciados parciales o totales del embalse
                    </div>
                    <p>
                        Como se anticipó, la estructura que funciona como descargador de fondo, se encuentra conformado por orificios emplazados dentro del cimacio del vertedero. Se compone de los siguientes elementos:
                        <br >
                        &nbsp;&nbsp;&nbsp;&nbsp;• 6 orificios de 2,60 m x 2,25 m (2 por vano, en 3 vanos)
                        <br >
                        &nbsp;&nbsp;&nbsp;&nbsp;• Compuertas de servicio y emergencia
                        <br >
                        &nbsp;&nbsp;&nbsp;&nbsp;• Curva de empalme
                        <br >
                        &nbsp;&nbsp;&nbsp;&nbsp;• Cuenco disipador compartido con el vertedero.
                        <br >
                    </p>

                    <h5>Obras de desvío del río durante la construcción</h5>
                    <div class="resumen">
                        Realizar la conexión terrestre del aprovechamiento con la red vial existente.
                    </div>
                    <p>
                        La obra de desvío se compone de un canal a cielo abierto que conduce el caudal desde el cauce natural de río, hasta la estructura de control. Se plantea un canal de sección trapezoidal, con ancho de solera de 120 m, con taludes 1V:2,5H. La estructura de control mencionada, es la que posteriormente se convertirá en el descargador de fondo y vertedero. En este caso, se utilizarán los 5 vanos. Para la reconversión, se obturan los vanos ubicados sobre la izquierda del canal.
                        <br >
                        Para este aspecto constructivo, pueden distinguirse dos etapas:
                        <br >
                        &nbsp;&nbsp;&nbsp;&nbsp;• 1era Etapa: Excavación del Canal de Desvío y construcción del vertedero con los 10 orificios en el cuerpo de hormigón, los cuales serán utilizados en la segunda etapa para el desvío del río. En esta etapa, el río se mantendrá en su curso natural por la margen izquierda del valle.
                        <br >
                        &nbsp;&nbsp;&nbsp;&nbsp;• 2da Etapa: Finalizada la construcción del vertedero y la excavación del canal de desvío, se procede al cierre del cauce natural mediante una ataguía de materiales sueltos, permitiendo el desvío del río a través del canal excavado y los 10 orificios construidos en el cuerpo del vertedero.                        
                        <br >
                    </p>

