            <h3 style="margin-bottom: 6px;">CÓNDOR CLIFF</h3>
            <h4>Descripción de cada componente</h4>
            
            <div class="row" style="padding-left: 2em; padding-right: 2em;">
                <div class="col-md-6 parrafos">
                    <h5>Presas de materiales sueltos con pantalla impermeable de hormigón.</h5>
                    <div class="resumen">
                        Es la estructura que constituye el cierre transversal del río, mediante la cual es posible elevar el nivel natural del río, y contener el embalse.
                    </div>
                    <p>
                        La presa es del tipo CFRD (concrete face rockfill dam), es decir, una presa de materiales sueltos con pantalla de hormigón, construida con materiales naturales procedentes de yacimientos cercanos al lugar. Se desarrolla en una longitud de 1613 m. El coronamiento posee un ancho de 12 m a un nivel de 180,6 m.s.n.m. Sobre el mismo se dispone de una calzada pavimentada de 7,30 m, con su correspondiente vereda de 2,00 m. Hacia aguas arriba, se coloca un muro rompeolas que también cumple la función de baranda. El volumen de terraplén es de aproximadamente 10,6 millones de metros cúbicos.
                        <br />
                        La altura de la presa desde su coronamiento hasta el fondo del cauce es de 68 m. Se han adoptado taludes con pendientes de 1V:1,5H, conforme a experiencias obtenidas de la construcción de presas similares. 
                        <br />
                        En particular, esta presa se funda sobre el aluvión del río. Por este motivo, se ejecutará un muro colado de hormigón armado de 0,80 m de espesor ejecutado por medio de paneles de 6,00 m de ancho con el objetivo de efectuar el cierre del escurrimiento subterráneo. Se prevé que este muro penetre en la roca una profundidad de 7 m.
                        <br />
                    </p>

                    <h5>Obras de toma para la casa de máquinas.</h5>
                    <div class="resumen">
                        Estructura destinada a conducir los caudales necesarios para la generación eléctrica a través de la tubería forzada hacia la casa de máquinas 
                    </div>
                    <p>
                        Se ubica a pie de presa, y se compone de un sector principal constituido por 5 módulos de 27 m de ancho cada uno, donde se alojan los 5 grupos turbogeneradores, cada uno de 180 MW, la nave de montaje de 48 m y una nave auxiliar de servicios de 12 m de ancho. Hacia aguas abajo, por encima del nivel de generadores se encuentra el sector de emplazamiento de transformadores y por debajo las galerías eléctricas y mecánicas.
                        <br />
                        El acceso se realiza desde el extremo de margen derecha, donde se ubica el ingreso a la nave de montaje, y al edificio que donde se brinda soporte a las operaciones, como así también la sala de control. 
                        <br />
                        A nivel 123,77 msnm, se encuentra la sala de generadores. Por sobre la misma se encuentra un puente grúa, equipado con un gancho principal y un gancho auxiliar, que se desplaza sobre las unidades y la nave de montaje. Este equipamiento se utiliza tanto para el montaje como para tareas de mantenimiento. En el sector exterior, se ubican 3 transformadores con sus respectivos pórticos de salida. Hacia aguas abajo se encuentra un pórtico grúa que se utiliza para la colocación de las ataguías del tubo de aspiración, cuando así lo requieran las tareas de mantenimiento.
                        <br />
                        En niveles inferiores se ubican dos galerías eléctricas, donde desarrollan su recorrido las barras de salida hacia los transformadores. Adicionalmente se ubican los diversos equipos eléctricos, como tableros, que acompañan a las instalaciones de este tipo. En un nivel inferior se ha proyectado una galería mecánica. En la misma se distribuyen los distintos sistemas auxiliares como por ejemplo, circuitos de aire comprimido, aceite, agua, etc. A su vez, se dispone de áreas que podrán ser utilizadas como talleres, tanto eléctricos como mecánicos o por ejemplo, pañoles donde se almacenan repuestos.
                        <br />
                    </p>

                    <h5>Descargadores de fondo.</h5>
                    <div class="resumen">
                        Órgano de seguridad, que tiene como función evacuar caudales a través del sector inferior del cierre. Se utiliza para realizar vaciados parciales o totales del embalse
                    </div>
                    <p>
                        La estructura del descargador de fondo se ubica sobre la margen derecha de la presa y consta de una estructura de hormigón con capacidad para erogar hasta 700 m3/s. En la misma se distinguen los siguientes componentes:
                        <br >
                        <br >

                        &nbsp;&nbsp;&nbsp;&nbsp;• Cuatro conductos de hormigón de sección rectangular, de funcionamiento independiente, controlados por compuertas planas de 2 m de ancho por 3 m de altura.
                        <br >
                        &nbsp;&nbsp;&nbsp;&nbsp;• En cada conducto, aguas abajo de la compuerta, se conforma un canal cerrado de sección rectangular de 5,8 m que incluye dispositivos para la incorporación forzada de aire
                        <br >
                        &nbsp;&nbsp;&nbsp;&nbsp;• Un cuenco disipador único conformado por una solera plana de 40 de longitud que dispone de bloques iniciales e intermedios y un diente final a la salida, que aumenta la disipación de energía
                        <br >
                        &nbsp;&nbsp;&nbsp;&nbsp;• Un canal de descarga a cielo abierto y sin revestir que restituye los caudales al cauce del río.
                        <br >
                        <br >
                    </p>

                    <img src="./images/proyecto_figura8.png" style="width: 100%; height: auto;" />
                    <div class="pie-imagen">
                        Figura 8 -CC – Descargador de fondo - Embocadura
                    </div>
                    
                    <img src="./images/proyecto_figura9.png" style="width: 100%; height: auto;" />
