            <a id="condor"></a>
            <h3 style="margin-bottom: 26px;">CÓNDOR CLIFF</h3>

            <div class="row" style="padding-left: 2em; padding-right: 2em;">
                <div class="col-md-6 parrafos">
                    La presa Cóndor Cliff, de 2.044 m de longitud, es de materiales sueltos con pantalla
                    impermeable de hormigón (o CFRD: concrete face rockfill dam) en su cara aguas arriba.
                    Los materiales provistos por la naturaleza no sufren ningún proceso químico de
                    transformación, siendo tratados y colocados mediante procedimientos de compactación
                    propios de la mecánica de suelos. Intervienen en su composición piedras, gravas, arenas,
                    limos y arcillas.
                    <br />
                    <br />
                    Cuenta con un vertedero (dispositivo de seguridad para evacuar el agua en condiciones
                    extraordinarias) que se ubica sobre la margen izquierda y se compone de cuatro vanos,
                    con una capacidad de descarga total de 4.163 m3/s, que corresponde a un caudal de
                    recurrencia de 10.000 años (es decir que se toma en cuenta una crecida que,
                    estadísticamente, ocurre una vez cada 10.000 años). El vertedero posee compuertas,
                    ataguías para realizar tareas de mantenimiento y un puente de servicio que brinda
                    conectividad a lo largo del coronamiento.
                    <br />
                    <br />
                    La obra de toma, que conduce el agua hasta la casa de máquinas, se compone de cinco
                    módulos de 25 m de ancho, uno para cada turbina. Cada módulo tiene rejas para evitar el
                    ingreso de elementos extraños, compuertas y ataguías de mantenimiento. El caudal que
                    ingresa es conducido hasta las turbinas ubicadas en la casa de máquinas mediante
                    tuberías forzadas de 9 m de diámetro y 214 m de longitud.
                    <br />
                    <br />
                    La casa de máquinas cuenta con cinco turbinas del tipo Francis de 190 MW cada una (950
                    MW en total), lo que implica una generación media anual de 3.190 GWh, operando en
                    régimen de punta. Es decir, estos equipos tendrán una utilización variable, generando
                    entre seis y ocho horas diarias, atendiendo los periodos de mayor demanda energética. La
                    casa de máquinas incluye una nave de montaje, una nave auxiliar de servicios y un sector
                    de galerías mecánicas y eléctricas para alojar al equipo auxiliar de generación.
                    <br />
                    <br />
                </div>
                <div class="col-md-6 parrafos">
                    El descargador de fondo, otro de los dispositivos de seguridad para la evacuación de agua
                    en casos de necesidad, se ubica sobre la margen derecha de la presa y tiene una
                    capacidad para erogar hasta 700 m3/s.
                    <br />
                    <br />
                    Además, la presa posee una escala de peces, ubicada sobre la margen izquierda, cuyo
                    objeto es posibilitar la migración hacia aguas arriba de la fauna íctica. Esta demandará el
                    uso de un caudal de 7 m3/s para su funcionamiento.
                    <br />
                    <br />
                    <b>Obra de desvío</b>
                    <br />
                    <br />
                    Para posibilitar la construcción de la presa, el río será desviado sobre la margen derecha a
                    través de un canal con una capacidad de conducción de 2.100 m3/s, correspondiente a un
                    caudal de recurrencia de 25 años. El desvío es un proceso que se subdivide en cuatro
                    etapas:
                    <br />
                    <br />
                    1) excavación en roca del canal sobre la margen derecha, emplazando dos ataguías de
                    materiales sueltos aguas arriba y aguas debajo de este para que el canal quede aislado del
                    río;
                    <br />
                    <br />
                    2) construcción de la estructura del canal en hormigón armado;
                    <br />
                    <br />
                    3) apertura de ambas ataguías y cierre del río, para promover el escurrimiento del agua
                    por el desvío, de manera tal que la zona de obra quede seca para poder realizar los
                    trabajos. Una vez que la obra civil haya concluido se procede a la etapa siguiente;
                    <br />
                    <br />
                    4) cierre de cuatro de los ocho módulos del canal y transformación de los cuatro restantes
                    en el descargador de fondo de la presa.
                    <br />
