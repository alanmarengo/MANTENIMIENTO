            <a id="descripcion"></a>
            <h3>DESCRIPCIóN</h3>

            <div class="row">
                <div class="col-md-6 text-justify">
                    El proyecto Aprovechamiento Hidroeléctrico del río Santa Cruz (AHRSC), es un emprendimiento hidroeléctrico estratégico para el desarrollo del país que se enmarca dentro de los preceptos de la energía renovable y se fundamenta bajo criterios de sostenibilidad.
                    <br />
                    IEASA, como organización pública que gestiona el proyecto, se compromete en un proceso de gestión del conocimiento abierto, de uso libre y gratuito, aportando visibilidad de sus acciones y acceso a la información para la aplicación en soluciones y toma de decisiones para el bien público.
                    <br />
                    En 2017 se presentó en Audiencia Pública en el Congreso de la Nación, prevista en la Ley Nº 23.879, el Estudio de Impacto Ambiental (EsIA) que permitió respaldar la factibilidad técnica, económica y ambiental del Proyecto. 
                    <br />
                    En ese marco, se instrumenta el Plan de Gestión Ambiental-PGA que identifica y organiza las recomendaciones del Estudio de Impacto Ambiental. Allí se instaura como compromiso el desarrollo de un Observatorio web para difundir la actuación, labor y la información de los planes y programas llevados a cabo. Posteriormente, este Observatorio,  se reestructura para contemplar una visión integral del proyecto hidroeléctrico y un entendimiento amplio  contribuyendo a la gestión de la sostenibilidad.
                    <br />
                    El Observatorio AHRSC es un espacio que reúne y articula herramientas basadas en conceptos propios de la gestión del conocimiento interdisciplinar, de la vigilancia estratégica y las redes de colaboración y uso compartido del conocimiento. Su estructura y componentes, concebidos como un sistema integral, permiten obtener una comprensión amplia de los procesos que hacen al estudio y construcción de una obra hidroeléctrica y a la ejecución de los compromisos ambientales en favor de la sociedad y de la naturaleza.
                    <br />
                </div>
                <div class="col-md-6 text-justify">
                    La gestión del conocimiento del proyecto se concibe como un proceso que va incrementando la escala de las intervenciones y del volumen de información. Se hace necesario promover una aplicación adaptada para las grandes escalas de información, contribuyendo a  facilitar su disponibilidad y el rápido acceso para su diligencia. Este es el caso de la generación de una suma de saberes que se acumula y que va adecuando en el tiempo los objetivos de su uso. La aplicación de la información se adapta a cada circunstancia en las etapas de construcción y de operación de un aprovechamiento hidroeléctrico. 
                    <br />
                    La gestión del AHRSC valoriza una gestión participativa del conocimiento. Es sensible al análisis y recopila, en forma sistemática y permanente, datos e información con el propósito de evaluar y seguir el complejo de acciones y procesos en torno a la construcción de un aprovechamiento hidroeléctrico. Esta actitud frente al conocimiento, se prolonga a su difusión pública, convirtiendo a la sociedad en el primer observador de esta gestión, y facilitando el medio para conocer los pareceres de los actores directa o indirectamente involucrados en su desarrollo.
                    <br />
                    El Observatorio, materializa un enfoque y una visión de la tutela y administración del proyecto AHRSC, manifiestando una clara posición a favor de la libre circulación de información.  Difunde el intercambio del conocimiento en red y lo capitaliza en la generación de nuevo conocimiento en apoyo a políticas especificas.
                    <br />
                    El Observatorio AHRSC desarrolla su capacidad con independencia en su tarea de investigación y análisis, y en la colaboración con órganos gubernamentales nacionales, provinciales, regionales y locales a los que destina el conocimiento para ser aplicados en políticas de desarrollo regional.
                    <br />
                </div>
            </div>
