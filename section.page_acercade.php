            <h3>Tratamiento de la información</h3>

            <div class="row" style="padding-left: 2em; padding-right: 2em;">
                <div class="col-md-6 parrafos">
                    <p>
                        La plataforma integra investigaciones e informes generados por especialistas directamente vinculados al proyecto, además de aquellos de consultoría externa para temas específicos por su importancia estratégica, registros audiovisuales, información georreferenciada, noticias, encuestas, datos estadísticos, planos, datos de registro de fenómenos con sensores de medición en tiempo real, que se integraron y compatibilizaron en una base de datos para su acceso y tratamiento a los distintos niveles de información.                     
                        <br />
                        <br />
                        <b>El elemento estructurador es el estudio-objeto, al que se vinculan e integran los recursos técnicos El estudio-objeto contiene los siguientes elementos:</b>
                    </p>

                    <img src="./images/bullet_blue.png" class="bullet" />
                    <p class="bullet-parrafo">
                        El o los documentos que aparecen como el producto parcial o final de un trabajo, 
                    </p>

                    <img src="./images/bullet_blue.png" class="bullet" />
                    <p class="bullet-parrafo">
                        La información geográfica que surge de los estudios documentados,
                    </p>

                    <img src="./images/bullet_blue.png" class="bullet" />
                    <p class="bullet-parrafo">
                        Los datos con y sin procesamiento relevados, 
                    </p>

                    <img src="./images/bullet_blue.png" class="bullet" />
                    <p class="bullet-parrafo">
                        Material audiovisual que se captura o genera en distintos momentos de los relevamientos,
                    </p>

                    <img src="./images/bullet_blue.png" class="bullet" />
                    <p class="bullet-parrafo">
                        Recursos técnicos específicos de los estudios como modelos, mapas temáticos, encuestas, noticias, perfiles, planos, imágenes satelitales, etc.
                    </p>
                </div>

                <div class="col-md-6 parrafos">
                    <p>
                        Todos los documentos y recursos técnicos asociados tienen asignados una codificación para generar las relaciones entre ellos y permitir múltiples asociaciones. La codificación se aplicó según los siguientes criterios:
                    </p>

                    <img src="./images/bullet_blue.png" class="bullet" />
                    <p class="bullet-parrafo2">
                        Temporalidad
                    </p>

                    <img src="./images/bullet_blue.png" class="bullet" />
                    <p class="bullet-parrafo2">
                        Espacialidad
                    </p>

                    <img src="./images/bullet_blue.png" class="bullet" />
                    <p class="bullet-parrafo2">
                        Tema y subtema
                    </p>

                    <img src="./images/bullet_blue.png" class="bullet" />
                    <p class="bullet-parrafo2">
                        Matriz de asignación de responsables
                    </p>

                    <img src="./images/bullet_blue.png" class="bullet" />
                    <p class="bullet-parrafo2">
                        Autores e investigadores a cargo
                    </p>

                    <br />
                    <p>
                        En el marco de la sistematización y normalización de la información de los AHRSC se identificó la información con la que se contaba  y se evaluó su utilidad. Este recurso es la primera aproximación a una representación abstracta y simplificada de la realidad en una estructura que organiza los tipos de objetos espaciales y la información, sus definiciones y características (atributos, relaciones y posibles operaciones). Se definió un Catálogo de Objetos Geográficos de los AHRSC conectado al catálogo de Metadatos implementado que permitió concretar una estructura interna de la información espacial y no espacial para mejorar el rendimiento en el uso de la información y lograr integridad en su totalidad. Se definieron los objetos geográficos como nivel básico y  su relación a otros niveles de información,  temas y subtemas para definir de manera concisa la especificación de un objeto geográfico y la información asociada. 
                    </p>

                </div>
