            <a id="tecnologias"></a>
            <h3 style="margin-bottom: 6px;">Tecnologías aplicadas</h3>
            <h4>para implementar el Observatorio de los AHRSC</h4>
            
            <div class="row" style="padding-left: 2em; padding-right: 2em;">
                <div class="col-md-6 parrafos">
                    <p>
                        El Observatorio AHRSC se desarrolló a medida en una plataforma de código libre (open source) que se asienta en los estándares internacionales de publicación de datos y servicios para su visualización. 
                    </p>

                    <h5>Base de datos espacial:</h5>
                    <p>
                        <b>- PostgresQL 9.2.24 version en Red Hat 4.8.5-28, modulo espacial PostGIS versión: 2.0.7:</b> Motor de base de datos relacional open source                     
                    </p>

                    <h5>Servidores de mapas web:</h5>
                    <p>
                        <b>- GeoServer versión: 2.15.1:</b> Software open source que dispone de una interfaz para gestionar capas de información geográfica en el servidor y aplica protocolos de intercambio de la Open Geospatial Consortium (OGC): WMS, WFS, WCS y CSW.
                        <br />
                        <b>- MapServer:</b> Servidor de mapas con interface cgi-bin/mapscript (PHP, Python, Perl, Ruby, Java, y .NET) con soporte para las especificaciones Catálogo de Metadatos y aplica los estándares OGC.
                        <br />
                        <b>- GeoNetwork versión: 3.4.4.0:</b> Software catalogador de metadatos con capacidad de gestión en una interfaz web y almacenado en base de datos.                    
                        <br />
                    </p>

                    <h5>Software de escritorio de sistemas de información Geográfica:</h5>
                    <p>
                        <b>- QGIS versión 3.4:</b>  Software open source que aplica estándares OGC y gestiona información vectorial y raster.
                    </p>
